#include <unistd.h>

void	writer(char a, char b, char c)
{
	char	vergule;
	char	space;

	vergule = ',';
	space = ' ';
	write(1, &a, 1);
	write(1, &b, 1);
	write(1, &c, 1);
	if (a + b + c != 168)
	{
		write(1, &vergule, 1);
		write(1, &space, 1);
	}
}

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;

	a = '0';
	b = '0';
	c = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				writer (a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}

